#type casting : simply converts one data type into another datatype.
# string to integer
int_num=120
string=int(int_num)
print(string,type(string))

#int to float
x=5
float_num=float(x)
print(float_num,type(float_num))

#number to sting

num=99.99
string_is=str(num)
print(string_is,type(string_is))

#string to boolean

string_is= "hello"
boolean=bool(string_is)
print(boolean,type(boolean))

#list to tuple
list_is=["one","two","three"]
con_tuple=tuple(list_is)
print(con_tuple,type(con_tuple))

